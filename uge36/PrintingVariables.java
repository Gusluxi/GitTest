public class PrintingVariables{

    public static void main(String[] args) {
        // Declaring Variables
	int hour = 11;
	int minute = 23;

	// Print
	System.out.println("Time is ");
	System.out.print(hour);
	System.out.print(":");
	System.out.print(minute);
	System.out.println(".");
    }
}